using System;
using System.Collections;

namespace wojilu {

    /// <summary>
    /// 分页后的结果集
    /// </summary>
    public interface IPageList {

        /// <summary>
        /// 当前页的数据列表
        /// </summary>
        IList Results { get; set; }

        /// <summary>
        /// 所有记录数
        /// </summary>
        int RecordCount { get; set; }

        /// <summary>
        /// 总页数
        /// </summary>
        int PageCount { get; set; }

        /// <summary>
        /// 每页数
        /// </summary>
        int Size { get; set; }

        /// <summary>
        /// 当前页码
        /// </summary>
        int Current { get; set; }

        /// <summary>
        /// 已经封装好的html分页栏
        /// </summary>
        String PageBar { get; set; }


    }

}

